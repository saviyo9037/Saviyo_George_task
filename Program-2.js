
function integer(num){
    let arr=[]

    for(let i=0;i<num; i++){
        arr.push(2 * i + 1)
    }
    return arr.join(", ")
}
console.log(integer(1));
console.log(integer(2));
console.log(integer(3));
console.log(integer(4));
console.log(integer(5));
console.log(integer(6));





